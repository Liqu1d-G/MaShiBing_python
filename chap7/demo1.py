'''
字典的创建方式
第一种方式，可以使用{}创建字典
第二种方式，可以使用内置函数dict
创建空字典，直接使用{}
'''

score = {'张三' : 100, '李四' : 90, '王五': 45}
print(score)  # {'张三': 100, '李四': 90, '王五': 45}

student = dict(name='Jack', age=20)
print(student)  # {'name': 'Jack', 'age': 20}

d = {}
print(d)  # {}


